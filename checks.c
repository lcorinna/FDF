#include "fdf.h"

int	ft_check_extensions(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && str[i] != '.')
		i++;
	if (str[i] == '.')
		i++;
	if (str[i] == '\0')
		return (1);
	if (str[i] == 'f' && str[i + 1] == 'd' && str[i + 2] == 'f' \
	&& str[i + 3] == '\0')
		return (0);
	return (1);
}
